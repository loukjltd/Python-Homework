print("Welcome to programming in Python")
print("This program shows If statements")

# Get input from User
num1 = int(input("Enter a number between 0 And 100: "))
num2 = int(input("Enter another number between 0 and 100: "))

# Test the numbers using relationship operators
if num1 == num2:
    print("The numbers are the same")

if num1 != num2:
    print("The numbers are not equal to each other")

if num1 >= num2:
    print("Number 1 is greater than or equal to number 2")

if num1 <= num2:
    print("Number 1 is less than or equal to number 2")
